function [nablaf] = grad(fhand, x)

    n = 2;
    delta = 0.000000001;

    nargin(fhand);

    for ii = 1:n
        Delta = zeros(n,1)';
        Delta(ii) = delta;
        dfdx(ii) = (fhand(x+Delta)-fhand(x)) / delta;
    end

    nablaf=dfdx;

end
